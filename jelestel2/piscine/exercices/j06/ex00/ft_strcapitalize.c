char	*ft_strcapitalize(char *str)
{
	int	i;

	i = -1;
	while (str[i + 1])
	{
		if ((str[i + 1] >= 'a') && (str[i + 1] <= 'z')
			&& ((str[i] == ' ') || (str[i] == '+') || (i < 0)
			|| (str[i] == '-') || (str[i] == '\t') || (str[i] == '\n')
			|| (str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r')))
			str[i + 1] -= 32;
		else if ((((str[i] <= 'z') && (str[i] >= 'a')) || ((str[i] >= 'A')
				&& (str[i] <= 'Z')) || ((str[i] >= '0') && (str[i] <= '9')))
				&& ((str[i + 1] >= 'A') && (str[i + 1] <= 'Z')))
			str[i + 1] += 32;
		i++;
	}
	return (str);
}
