#include "ft_ls.h"

int			ft_dec_to_oct(int n)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (n > 0)
	{
		i = i + ((n % 8) * ft_power(10, j));
		n = n / 8;
		j++;
	}
	return (i);
}


