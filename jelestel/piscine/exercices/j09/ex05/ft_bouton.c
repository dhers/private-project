int		ft_bouton(int i, int j, int k)
{
	int	mid;

	if ((i <= j && j <= k) || (k <= j && j <= i))
		mid = j;
	else if ((j <= i && i <= k) || (k <= i && i <= j))
		mid = i;
	else
		mid = k;
	return (mid);
}
